const attrMap = [
  ['data-i18n-placeholder', 'placeholder'],
  ['data-i18n-aria', 'aria-label'],
  ['data-i18n-title', 'title']
];

export function applyI18n(lang = 'fr') {
  const safeLang = translations[lang] ? lang : 'fr';
  const dict = translations[safeLang];
  const dir = safeLang === 'ar' ? 'rtl' : 'ltr';

  document.documentElement.lang = safeLang;
  document.documentElement.dir = dir;
  document.body?.setAttribute('dir', dir);

  const select = document.getElementById('lang-select');
  if (select && select.value !== safeLang) select.value = safeLang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const value = dict[el.dataset.i18n];
    if (value) el.textContent = value;
  });

  attrMap.forEach(([dataAttr, targetAttr]) => {
    document.querySelectorAll(`[${dataAttr}]`).forEach(el => {
      const value = dict[el.getAttribute(dataAttr)];
      if (value) el.setAttribute(targetAttr, value);
    });
  });

  localStorage.setItem('ahado_lang', safeLang);
  window.dispatchEvent(new CustomEvent('ahado:langchange', { detail: { lang: safeLang, dir } }));
}

export function getLang() {
  return new URLSearchParams(location.search).get('lang') || localStorage.getItem('ahado_lang') || 'fr';
}
